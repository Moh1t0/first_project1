package org.javaacademy.core.homework.homework3.ex2.phone.components;

public class Frame {
    private int width;
    private int height;
    private int length;

    public Frame(int width, int height, int length) {
        this.width = width;
        this.height = height;
        this.length = length;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getLength() {
        return length;
    }
}
